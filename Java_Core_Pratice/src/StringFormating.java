
public class StringFormating {

	public static void main(String[] args) {
		System.out.printf("FirstName\t\tLastName\t\tClass\n");
		System.out.printf("%-10s\t\t%-10s\t\t%-2d\n","Ram","Basu",9);
		System.out.printf("%-10s\t\t%-10s\t\t%-2d\n","Shyam","Thapa",10);
		System.out.printf("%-10s\t\t%-10s\t\t%-2d\n","Gita","Rai",10);
		System.out.printf("%-10s\t\t%-10s\t\t%-2d\n","kamless","swual",8);
	}

}
